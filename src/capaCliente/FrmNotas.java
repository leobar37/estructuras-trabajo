    private void buttonAero1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAero1ActionPerformed
        DefaultTableModel modelo=new DefaultTableModel();
        DefaultTableModel modelo1=new DefaultTableModel();
        modelo.addColumn("DNI");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("NOTA");
        modelo1.addColumn("DNI");
        modelo1.addColumn("NOMBRE");
        modelo1.addColumn("APELLIDO");
        modelo1.addColumn("NOTA");
        Object datos[]=new Object[4];
        Object datos1[]=new Object[4];
        String tipo=cboTipoCalificacion.getSelectedItem().toString();
        String nivel=cboNivel.getSelectedItem().toString();
        int  mes=Integer.parseInt(txtMI.getText());
         Object lista[]=Calificacion.mayorNota(mes, nivel, tipo);
         ListasAux.ListaEnlazada listaMayor = (ListasAux.ListaEnlazada) lista[0];
         ListasAux.ListaEnlazada listaMenor = (ListasAux.ListaEnlazada) lista[1];
        
             for (int i = 0; i < listaMayor.tamaño(); i++) {
            Calificacion obj=(Calificacion)listaMayor.Buscar(i);
            Alumno obj1=(Alumno)Alumno.ObtnerAlumno(obj.getIdAlumno());
            datos[0]=obj1.getDni();
            datos[1]=obj1.getNombre();
            datos[2]=obj1.getApellidos();
            datos[3]=obj.getNota();
            modelo.addRow(datos);
        } 
                         
   
        tblMayor.setModel(modelo);
        for (int i = 0; i < listaMenor.tamaño(); i++) {
            Calificacion obj=(Calificacion)listaMenor.Buscar(i);
            Alumno obj1=(Alumno)Alumno.ObtnerAlumno(obj.getIdAlumno());
            datos1[0]=obj1.getDni();
            datos1[1]=obj1.getNombre();
            datos1[2]=obj1.getApellidos();
            datos1[3]=obj.getNota();
            modelo1.addRow(datos1);
        }
        tblMenor.setModel(modelo1);

    }//GEN-LAST:event_buttonAero1ActionPerformed

