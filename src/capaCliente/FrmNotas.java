    private void cboProgramaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboProgramaItemStateChanged
        //Nivel
        if (cboPrograma.getSelectedItem().toString().equalsIgnoreCase("Niños")){
            cboNivel.setModel(new DefaultComboBoxModel<>(new String []{"Seleccione","kinder", "pre kids", "kids", "children"}));
        }else
        if(cboPrograma.getSelectedItem().toString().equalsIgnoreCase("Adolecentes")){
            cboNivel.setModel(new DefaultComboBoxModel<>(new String []{"Seleccione","Adolecentes"}));
        }else
        if(cboPrograma.getSelectedItem().toString().equalsIgnoreCase("Adultos")){
            cboNivel.setModel(new DefaultComboBoxModel<>(new String []{"Seleccione","Beginner-A1", "Elementary-A2", "Pre Intermediate-B1", "Intermediate-B2", "Upper Intermediate-C1", "Advanced C2"}));
        }

    }//GEN-LAST:event_cboProgramaItemStateChanged

